package Model;

public class Superlative {
    private float A;
    private float B;

    public Superlative(float a, float b) {
        A = a;
        B = b;
    }

    public float getA() {
        return A;
    }

    public void setA(float a) {
        A = a;
    }

    public float getB() {
        return B;
    }

    public void setB(float b) {
        B = b;
    }
}
